#!/usr/bin/env node
/**
 * TEMPORARY — delete this file (and SetupTemp.js + filePushOrder entries) when Script Properties are set.
 *
 * Writes scripts/generated/tt-manual-script-properties.md with /exec URLs and property tables to paste
 * in Project Settings → Script properties. No API executable or clasp run — production is **web app only**.
 *
 * Required env:
 *   export TT_AUTH_INTERNAL_SECRET='shared-secret-for-auth-and-broker'
 * Optional:
 *   export TT_TOKEN_DEMO='value'   → shown for broker TOKEN_DEMO
 *
 * Usage: npm run setup:urls
 */

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const genDir = join(__dirname, 'generated');

const ids = JSON.parse(
  readFileSync(join(__dirname, 'tt-deploy-ids.json'), 'utf8')
);

const authExec = `https://script.google.com/macros/s/${ids.authenticatorDeploymentId}/exec`;
const brokerExec = `https://script.google.com/macros/s/${ids.tokenBrokerDeploymentId}/exec`;

const secret = process.env.TT_AUTH_INTERNAL_SECRET;
if (!secret || !String(secret).trim()) {
  console.error(
    'Required: export TT_AUTH_INTERNAL_SECRET=<shared secret for Authenticator + Token broker>'
  );
  process.exit(1);
}

const demoToken = process.env.TT_TOKEN_DEMO;

mkdirSync(genDir, { recursive: true });

const manualMd = `# Token Triangle — Script Properties (paste in each project)

Generated by scripts/tt-setup-deploy-urls.mjs — delete after use.

## Web app URLs (/exec)

| Role | URL |
|------|-----|
| Authenticator | ${authExec} |
| Token broker | ${brokerExec} |

## Project editor links

- [Authenticator](https://script.google.com/home/projects/${ids.authenticatorScriptId}/edit)
- [Token broker](https://script.google.com/home/projects/${ids.tokenBrokerScriptId}/edit)
- [Sample caller](https://script.google.com/home/projects/${ids.sampleCallerScriptId}/edit)

(⚙ **Project Settings** → **Script properties** in each.)

---

### Authenticator — property

| Property | Value |
|----------|-------|
| \`AUTH_INTERNAL_SECRET\` | \`${secret}\` |

---

### Token broker — properties

| Property | Value |
|----------|-------|
| \`AUTHENTICATOR_BASE_URL\` | \`${authExec}\` |
| \`AUTH_INTERNAL_SECRET\` | \`${secret}\` |
| \`TOKEN_DEMO\` | \`${demoToken || '(set a secret string for logical name DEMO)'}\` |

---

### Sample caller — properties

| Property | Value |
|----------|-------|
| \`AUTHENTICATOR_URL\` | \`${authExec}\` |
| \`TOKEN_BROKER_URL\` | \`${brokerExec}\` |
`;

const outPath = join(genDir, 'tt-manual-script-properties.md');
writeFileSync(outPath, manualMd, 'utf8');
console.log(`Wrote ${outPath}`);
console.log(`
Web app /exec URLs (versioned deployments — see AGENTS.md):
  Authenticator: ${authExec}
  Token broker:  ${brokerExec}

Optional: run \`_ttSetup*\` from the script editor (function dropdown + Run) using SetupTemp.js — no API executable needed.

Remove when finished: scripts/tt-setup-deploy-urls.mjs, scripts/tt-deploy-ids.json, src/SetupTemp.js, and SetupTemp entries in .clasp.json filePushOrder.
`);
